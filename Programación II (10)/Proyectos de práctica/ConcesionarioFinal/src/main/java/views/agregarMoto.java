    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed
        try {
            String modelo = String.valueOf(txtModelo.getText());
            String marca = String.valueOf(ssMarca.getSelectedItem());
            String color = txtColor.getText();
            int kilometros = Integer.parseInt(intKm.getText());

            String cond;
             if(botonNuevo.isSelected() & kilometros == 0) {
                cond = "Nuevo";
            } else if (botonUsado.isSelected() & kilometros > 0){
                cond = "Usado";
            } else if (botonNuevo.isSelected() & kilometros != 0){
                cond = "Usado";
            } else if (botonUsado.isSelected() & kilometros == 0){
                cond = "Nuevo";
            } else {
                cond = "";
            }
            double potenciaMotor = Double.parseDouble(((intPotencia.getText())));
            double precio = Double.parseDouble(intPrecio.getText());
            String embrague = txtEmbrague.getText();
            int pasajeros = Integer.parseInt(intPasajeros.getText());

            if(kilometros < 0 || potenciaMotor < 0 || precio < 0 || pasajeros < 0) {
                throw new IllegalArgumentException();
            }

            vendedor.agregarMoto(modelo, marca, color, kilometros, cond, potenciaMotor, precio, embrague, pasajeros, vendedor.getIdVendedor());
            fun.mostrarMensaje("Vehiculo publicado con exito", "Info", "VEHICULO PUBLICADO");
        } catch (NumberFormatException e){
            fun.mostrarMensaje("Valores incorrectos", "Error", "VEHICULO NO PUBLICADO");
        } catch(IllegalArgumentException e){
            e.getMessage();
            fun.mostrarMensaje("Valores menores a cero", "Error", "VEHICULO NO PUBLICADO");
        }
    }//GEN-LAST:event_botonAgregarActionPerformed

