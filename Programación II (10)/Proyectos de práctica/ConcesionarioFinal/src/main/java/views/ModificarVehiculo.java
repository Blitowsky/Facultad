    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        
        String marca = txtMarca.getText();
        String modelo = txtModelo.getText();
        String color = txtColor.getText();
        int km = Integer.parseInt(String.valueOf(intKm.getText()));
        String cond = txtCond.getText();
        double pot = Double.parseDouble(String.valueOf(intPotencia.getText()));
        double precio = Double.parseDouble(String.valueOf(intPrecio.getText()));
        
       
        if (intPuertas.isVisible()) {
            try {
                int cantPuertas = Integer.parseInt(String.valueOf(intPuertas.getText()));
                if(km < 0 || pot < 0 || precio < 0 || cantPuertas <= 0) {
                    throw new IllegalArgumentException();
                }
                Auto auto = (Auto) vehiculo;
                vendedor.modificarVehiculo(auto, marca, modelo, color, km, cond, pot, precio, cantPuertas, null, null, 0);
            } catch (SQLException ex) {
                Logger.getLogger(ModificarVehiculo.class.getName()).log(Level.SEVERE, null, ex); 
            } catch(IllegalArgumentException e){
                e.getMessage();
                func.mostrarMensaje("Valores menores a cero", "Error", "VEHICULO NO PUBLICADO");
            }
        } else if (txtTraccion.isVisible()) {
            try {
                String traccion = txtTraccion.getText();
                if(km < 0 || pot < 0 || precio < 0 || precio <= 0) {
                    throw new IllegalArgumentException();
                }
                Camioneta camioneta = (Camioneta) vehiculo;
                vendedor.modificarVehiculo(camioneta, marca, modelo, color, km, cond, pot, precio, 0 ,traccion, null, 0);
            } catch (SQLException ex) {
                Logger.getLogger(ModificarVehiculo.class.getName()).log(Level.SEVERE, null, ex);
            } catch(IllegalArgumentException e){
                e.getMessage();
                func.mostrarMensaje("Valores menores a cero", "Error", "VEHICULO NO PUBLICADO");
            }
        } else {
            try {
                String embrague = txtEmbrague.getText();
                int cantPasajeros = Integer.parseInt(String.valueOf(intPasajeros.getText()));
                if(km < 0 || pot < 0 || precio < 0 || cantPasajeros < 0) {
                throw new IllegalArgumentException();
                }
                Moto moto = (Moto) vehiculo;
                vendedor.modificarVehiculo(moto, marca, modelo, color, km, cond, pot, precio, 0, null, embrague, cantPasajeros);
            } catch (SQLException ex) {
                Logger.getLogger(ModificarVehiculo.class.getName()).log(Level.SEVERE, null, ex);
            } catch(IllegalArgumentException e){
                e.getMessage();
                func.mostrarMensaje("Valores menores a cero", "Error", "VEHICULO NO PUBLICADO");
            }
        }
        this.dispose();
    }//GEN-LAST:event_btnModificarActionPerformed

