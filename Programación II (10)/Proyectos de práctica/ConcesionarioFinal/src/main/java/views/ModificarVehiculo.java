    private void cargarVehiculo(int id) throws SQLException {
        vehiculo = vendedor.traerVehiculo(id);
        vehiculo.setIdVehiculo(id);
        txtMarca.setText(vehiculo.getMarca());
        txtModelo.setText(vehiculo.getModelo());
        txtColor.setText(vehiculo.getColor());
        intKm.setText(String.valueOf(vehiculo.getKms()));
        txtCond.setText(vehiculo.getCondicion());
        intPotencia.setText(String.valueOf(vehiculo.getPotencia()));
        intPrecio.setText(String.valueOf(vehiculo.getPrecio()));

        if (vehiculo instanceof Auto auto) {
            labelPuertas.setVisible(true);
            intPuertas.setVisible(true);
            intPuertas.setText(String.valueOf(auto.getCantPuertas()));
        } else if (vehiculo instanceof Camioneta camioneta) {
            labelTracc.setVisible(true);  
            txtTraccion.setVisible(true);
            txtTraccion.setText(camioneta.getTipoTaccion());
        } else if(vehiculo instanceof Moto moto) {
            labelEmb.setVisible(true); 
            txtEmbrague.setVisible(true);
            txtEmbrague.setText(moto.getTipoEmbrague());
            labelPas.setVisible(true);
            intPasajeros.setVisible(true);
            intPasajeros.setText(String.valueOf(moto.getCantPasajeros()));  
        }
    }

