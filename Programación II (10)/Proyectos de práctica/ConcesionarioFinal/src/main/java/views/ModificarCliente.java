    private void botonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonModificarActionPerformed

        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String empresa = txtEmpresa.getText();
        String usuario = txtUsuario.getText();
        String contra = txtContrasena.getText();
        int dni;

        try {
            if (nombre.isEmpty() || apellido.isEmpty() || empresa.isEmpty() || usuario.isEmpty() || contra.isEmpty()) {
                throw new IllegalArgumentException("Todos los campos deben estar completos");
            }
            dni = Integer.parseInt(intDni.getText());
            boolean modificar = vendedor.modificarCliente(cliente, nombre, apellido, dni, empresa, usuario, contra);
            if(modificar) {
                fun.mostrarMensaje("Cliente modificado con √©xito", "Info", "Cliente modificado");
            } else {
                fun.mostrarMensaje("Usuario en uso", "Error", "CLIENTE NO MODIFICADO");
            }
        } catch (NumberFormatException e) {
            fun.mostrarMensaje("El DNI debe ser un n√∫mero v√°lido", "Error", "CLIENTE NO MODIFICADO");
        } catch (IllegalArgumentException e) {
            fun.mostrarMensaje(e.getMessage(), "Error", "CLIENTE NO MODIFICADO");
        } catch (SQLException ex) { 
            Logger.getLogger(ModificarCliente.class.getName()).log(Level.SEVERE, null, ex);
        }

        this.dispose();
    }//GEN-LAST:event_botonModificarActionPerformed
    

