    private void taerVendedor(int idVendedor) {
        vendedor = gerente.traerVendedor(idVendedor);
        vendedor.setIdVendedor(idVendedor);
        txtNombre.setText(vendedor.getNombre());
        txtApellido.setText(vendedor.getApellido());
        intDni.setText(String.valueOf(vendedor.getDni()));
        doubleSueldo.setText(String.valueOf(vendedor.getSueldo()));
        txtUsuario.setText(vendedor.getUsuario());
        txtContra.setText(vendedor.getContrasena());
    }
