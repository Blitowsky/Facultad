    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String usuario = txtUsuario.getText();
        String contra = txtContra.getText();
        int dni;
        double sueldo;
        try {
            if (nombre.isEmpty() || apellido.isEmpty() || usuario.isEmpty() || contra.isEmpty()) {
                throw new IllegalArgumentException("Todos los campos deben estar completos");
            }
            dni = Integer.parseInt(intDni.getText());
            sueldo = Double.parseDouble(doubleSueldo.getText());
            boolean modificar = gerente.modificarVendedores(vendedor, nombre, apellido, dni, sueldo, usuario, contra);
            if(modificar) {
                func.mostrarMensaje("Vendedor modificado con éxito", "Info", "Vendedor modificado");
            } else {
                func.mostrarMensaje("Usuario en uso", "Error", "VENDEDOR NO MODIFICADO");
            }
        } catch (NumberFormatException e) {
            func.mostrarMensaje("El DNI o el sueldo debe ser un número válido", "Error", "VENDEDOR NO MODIFICADO");
        } catch (IllegalArgumentException e) {
            func.mostrarMensaje(e.getMessage(), "Error", "VENDEDOR NO MODIFICADO");
        } catch (SQLException ex) { 
            Logger.getLogger(ModificarCliente.class.getName()).log(Level.SEVERE, null, ex);
        }

        this.dispose();
        
    }//GEN-LAST:event_btnModificarActionPerformed



